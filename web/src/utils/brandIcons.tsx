/**
 * 根据 issuer 名称生成一致的色相值（0-360）
 */
function stringToHue(str: string): number {
  let hash = 0
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash)
  }
  return Math.abs(hash % 360)
}

/**
 * 根据 issuer 生成首字母彩色圆
 */
function generateInitialAvatar(issuer: string) {
  const initial = issuer.charAt(0).toUpperCase()
  const hue = stringToHue(issuer)
  const bgColor = `hsl(${hue}, 65%, 50%)`
  const textColor = 'white'

  return (
    <div
      className="w-full h-full flex items-center justify-center rounded-full font-semibold text-xl"
      style={{ backgroundColor: bgColor, color: textColor }}
    >
      {initial}
    </div>
  )
}

/**
 * 获取品牌图标组件
 *
 * @param issuer - 服务名称（如 "GitHub", "Google"）
 * @returns React 组件（品牌 logo 或首字母圆）
 */
export function useBrandIcon(issuer: string): React.ReactNode {
  return useMemo(() => {
    const normalizedIssuer = issuer.toLowerCase().trim()

    // 匹配已知品牌
    if (BRAND_ICONS[normalizedIssuer]) {
      return (
        <div className="w-full h-full flex items-center justify-center">
          {BRAND_ICONS[normalizedIssuer]}
        </div>
      )
    }

    // 兜底：首字母彩色圆
    return generateInitialAvatar(issuer)
  }, [issuer])
}
